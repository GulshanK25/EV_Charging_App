import mongoose from "mongoose";

const { Schema, model } = mongoose;

const registrationSchema = new Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    address: {
        type: String,
        required: true
    }
});

const Registration = model("Registration", registrationSchema);

export default Registration;
